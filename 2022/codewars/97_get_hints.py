def get_hints(answer, guess):
    result = {'black': 0, 'white': 0}
    bd = {x: answer.count(x) for x in answer}
    for x, y in zip(answer, guess):
        if x == y:
            result['black'] += 1
            bd[x] -= 1
    for x, y in zip(answer, guess):
        if x!= y and y in bd.keys():
            if bd[y] > 0:
                result['white'] += 1
                bd[y] -= 1
    return result


