def interpreter(tape):
    memory = [0]
    result = []
    i = 0
    for x in tape:
        if x == '+':
            memory[i] += 1
        elif x == '-':
            memory[i] -= 1
        elif x == '<':
            if i - 1 == -1:
                memory.insert(0, 0)
            else:
                i -= 1
        elif x == '>':
            if i + 1 == len(memory):
                memory.append(0)
            i += 1
        elif x == '*':
            result.append(chr(memory[i]))
    return ''.join(result)


